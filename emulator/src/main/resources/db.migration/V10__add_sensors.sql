CREATE TABLE sensors (
	id int8 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	chemical_level float8 NULL,
	chemical_offset float8 NULL,
	humidity_offset int4 NULL,
	latitude_weather_geolocation float8 NULL,
	longitude_weather_geolocation float8 NULL,
	maunre_level float8 NULL,
	manure_offset float8 NULL,
	precipitation_amount_offset float8 NULL,
	pressure_offset int4 NULL,
	uuid uuid NULL,
	temp_offset float8 NULL,
	wind_speed_offset float8 NULL,
	CONSTRAINT sensors_pkey PRIMARY KEY (id)
);