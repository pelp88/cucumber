CREATE TABLE reading (
	id int8 NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	air_temperature float8 NULL,
	chemical_level float8 NULL,
	humidity int4 NULL,
	manure_level float8 NULL,
	precipitation_amount float8 NULL,
	pressure int4 NULL,
	serial_number uuid NULL,
	"time" timestamp NULL,
	wind_speed float8 NULL,
	CONSTRAINT reading_pkey PRIMARY KEY (id)
);